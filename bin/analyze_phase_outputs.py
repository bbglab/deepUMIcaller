#!/usr/bin/env python3
"""Analyze outputs from phase_mutations.py for two downstream goals.

Goals:
1) Detect chain imbalance patterns that can indicate putative copy-number changes.
2) Identify variants whose mutated reads are credibly present in both chains,
   consistent with independent recurrence of the same somatic event.

Input is the `*.variant_chain_support.tsv` table produced by phase_mutations.py.
"""

from __future__ import annotations

import argparse
import math
import re
from pathlib import Path
from typing import List, Tuple

import pandas as pd


REQUIRED_COLUMNS = {
    "variant_id",
    "CHROM",
    "POS",
    "REF",
    "ALT",
    "n_mut_reads",
    "n_ref_reads",
    "mut_chain_0",
    "mut_chain_1",
    "ref_chain_0",
    "ref_chain_1",
}


def parse_args() -> argparse.Namespace:
    parser = argparse.ArgumentParser(
        description=(
            "Analyze phased variant-chain support to infer chain imbalance "
            "(copy-number proxy) and detect likely independent recurrence events."
        )
    )
    parser.add_argument(
        "-i",
        "--input",
        required=True,
        help="Input TSV generated by phase_mutations.py (*.variant_chain_support.tsv).",
    )
    parser.add_argument(
        "-o",
        "--output-prefix",
        default=None,
        help="Output prefix. Defaults to input path without extension.",
    )

    parser.add_argument(
        "--window-size",
        type=int,
        default=5_000_000,
        help="Window size (bp) for local chain-imbalance summaries (default: 5,000,000).",
    )
    parser.add_argument(
        "--min-sites-per-segment",
        type=int,
        default=3,
        help="Minimum number of variant sites in a chromosome/window to emit a CNV proxy call.",
    )
    parser.add_argument(
        "--cnv-balance-tolerance",
        type=float,
        default=0.07,
        help="Absolute deviation from 0.5 below which segment is considered balanced.",
    )
    parser.add_argument(
        "--cnv-gain-threshold",
        type=float,
        default=0.67,
        help="Major-chain fraction threshold for putative gain-like imbalance.",
    )
    parser.add_argument(
        "--cnv-loss-threshold",
        type=float,
        default=0.85,
        help="Major-chain fraction threshold for putative loss/LOH-like imbalance.",
    )

    parser.add_argument(
        "--min-total-mut-reads",
        type=int,
        default=4,
        help="Minimum mutated reads at a site to evaluate independent recurrence.",
    )
    parser.add_argument(
        "--min-minor-mut-reads",
        type=int,
        default=2,
        help="Minimum mutated reads in the minor chain to consider recurrence.",
    )
    parser.add_argument(
        "--min-minor-mut-fraction",
        type=float,
        default=0.20,
        help="Minimum minor-chain mutated-read fraction for recurrence candidates.",
    )
    parser.add_argument(
        "--single-chain-major-prop",
        type=float,
        default=0.90,
        help=(
            "Major-chain mutated-read proportion threshold used to estimate background "
            "cross-chain noise from likely single-chain mutations."
        ),
    )
    parser.add_argument(
        "--min-mut-reads-for-error-estimation",
        type=int,
        default=4,
        help="Minimum mutated reads at a site when estimating background cross-chain error.",
    )
    parser.add_argument(
        "--fallback-error-rate",
        type=float,
        default=0.02,
        help="Fallback cross-chain error rate if data are insufficient for estimation.",
    )
    parser.add_argument(
        "--alpha",
        type=float,
        default=0.01,
        help="One-sided binomial significance threshold for recurrence candidates.",
    )

    return parser.parse_args()


def chrom_sort_key(chrom: str) -> Tuple[int, int | str]:
    text = str(chrom)
    m = re.match(r"^chr(\d+)$", text, flags=re.IGNORECASE)
    if m:
        return (0, int(m.group(1)))
    specials = {"chrx": 1000, "chry": 1001, "chrm": 1002, "chrmt": 1002}
    lower = text.lower()
    if lower in specials:
        return (1, specials[lower])
    return (2, lower)


def _binom_sf(k: int, n: int, p: float) -> float:
    """Exact survival function P(X >= k) for X ~ Binomial(n, p)."""
    if k <= 0:
        return 1.0
    if k > n:
        return 0.0
    if p <= 0:
        return 0.0
    if p >= 1:
        return 1.0

    tail = 0.0
    q = 1.0 - p
    for i in range(k, n + 1):
        tail += math.comb(n, i) * (p**i) * (q ** (n - i))
    return float(min(max(tail, 0.0), 1.0))


def classify_imbalance(
    major_fraction: float,
    min_sites_ok: bool,
    balance_tolerance: float,
    gain_threshold: float,
    loss_threshold: float,
) -> str:
    if not min_sites_ok:
        return "insufficient_sites"
    if pd.isna(major_fraction):
        return "not_informative"

    if major_fraction >= loss_threshold:
        return "putative_loss_or_loh_like"
    if major_fraction >= gain_threshold:
        return "putative_gain_like"
    if major_fraction > (0.5 + balance_tolerance):
        return "mild_imbalance"
    return "balanced"


def add_chain_coverage_fields(df: pd.DataFrame) -> pd.DataFrame:
    out = df.copy()
    numeric_cols = [
        "POS",
        "n_mut_reads",
        "n_ref_reads",
        "mut_chain_0",
        "mut_chain_1",
        "ref_chain_0",
        "ref_chain_1",
    ]
    for col in numeric_cols:
        out[col] = pd.to_numeric(out[col], errors="coerce").fillna(0)

    out["chain_0_total"] = out["mut_chain_0"] + out["ref_chain_0"]
    out["chain_1_total"] = out["mut_chain_1"] + out["ref_chain_1"]
    out["total_chain_reads"] = out["chain_0_total"] + out["chain_1_total"]
    out["site_major_total"] = out[["chain_0_total", "chain_1_total"]].max(axis=1)
    out["site_minor_total"] = out[["chain_0_total", "chain_1_total"]].min(axis=1)

    informative = out["total_chain_reads"] > 0
    out["chain_0_fraction"] = float("nan")
    out.loc[informative, "chain_0_fraction"] = out.loc[informative, "chain_0_total"] / out.loc[informative, "total_chain_reads"]
    return out


def summarize_segments(
    df: pd.DataFrame,
    group_cols: List[str],
    min_sites_per_segment: int,
    balance_tolerance: float,
    gain_threshold: float,
    loss_threshold: float,
) -> pd.DataFrame:
    grouped = (
        df.groupby(group_cols, as_index=False)
        .agg(
            n_sites=("variant_id", "count"),
            sum_site_major=("site_major_total", "sum"),
            sum_site_minor=("site_minor_total", "sum"),
            sum_total_reads=("total_chain_reads", "sum"),
        )
        .copy()
    )

    grouped["major_chain"] = "not_oriented"
    grouped["major_fraction"] = float("nan")
    grouped["minor_fraction"] = float("nan")
    grouped["imbalance_score"] = float("nan")
    grouped["chain_ratio_major_to_minor"] = float("nan")

    informative = grouped["sum_total_reads"] > 0
    major_reads = grouped["sum_site_major"]
    minor_reads = grouped["sum_site_minor"]

    grouped.loc[informative, "major_fraction"] = major_reads[informative] / grouped.loc[informative, "sum_total_reads"]
    grouped.loc[informative, "minor_fraction"] = minor_reads[informative] / grouped.loc[informative, "sum_total_reads"]
    grouped.loc[informative, "imbalance_score"] = (grouped.loc[informative, "major_fraction"] - 0.5).abs() * 2.0

    ratio_denom = minor_reads.replace(0, pd.NA)
    grouped["chain_ratio_major_to_minor"] = major_reads / ratio_denom

    grouped["cnv_proxy_call"] = grouped.apply(
        lambda r: classify_imbalance(
            major_fraction=r["major_fraction"],
            min_sites_ok=(int(r["n_sites"]) >= min_sites_per_segment),
            balance_tolerance=balance_tolerance,
            gain_threshold=gain_threshold,
            loss_threshold=loss_threshold,
        ),
        axis=1,
    )

    return grouped


def infer_cnv_proxy(
    df: pd.DataFrame,
    window_size: int,
    min_sites_per_segment: int,
    balance_tolerance: float,
    gain_threshold: float,
    loss_threshold: float,
) -> Tuple[pd.DataFrame, pd.DataFrame, pd.DataFrame]:
    informative_df = df[df["total_chain_reads"] > 0].copy()

    by_chrom = summarize_segments(
        informative_df,
        group_cols=["CHROM"],
        min_sites_per_segment=min_sites_per_segment,
        balance_tolerance=balance_tolerance,
        gain_threshold=gain_threshold,
        loss_threshold=loss_threshold,
    )

    if informative_df.empty:
        by_window = pd.DataFrame(
            columns=[
                "CHROM",
                "window_start",
                "window_end",
                "n_sites",
                "sum_site_major",
                "sum_site_minor",
                "sum_total_reads",
                "major_chain",
                "major_fraction",
                "minor_fraction",
                "imbalance_score",
                "chain_ratio_major_to_minor",
                "cnv_proxy_call",
            ]
        )
    else:
        informative_df["window_start"] = ((informative_df["POS"].astype(int) - 1) // window_size) * window_size + 1
        informative_df["window_end"] = informative_df["window_start"] + window_size - 1

        by_window = summarize_segments(
            informative_df,
            group_cols=["CHROM", "window_start", "window_end"],
            min_sites_per_segment=min_sites_per_segment,
            balance_tolerance=balance_tolerance,
            gain_threshold=gain_threshold,
            loss_threshold=loss_threshold,
        )

    chrom_calls = by_chrom[~by_chrom["cnv_proxy_call"].isin(["balanced", "insufficient_sites", "not_informative"])].copy()
    chrom_calls["resolution"] = "chromosome"

    window_calls = by_window[~by_window["cnv_proxy_call"].isin(["balanced", "insufficient_sites", "not_informative"])].copy()
    if not window_calls.empty:
        window_calls["resolution"] = "window"

    cnv_calls = pd.concat([chrom_calls, window_calls], ignore_index=True, sort=False)

    if "CHROM" in by_chrom.columns:
        by_chrom = by_chrom.sort_values("CHROM", key=lambda s: s.map(chrom_sort_key))
    if not by_window.empty and "CHROM" in by_window.columns:
        by_window = by_window.sort_values(["CHROM", "window_start"], key=lambda s: s.map(chrom_sort_key) if s.name == "CHROM" else s)
    if not cnv_calls.empty and "CHROM" in cnv_calls.columns:
        if "window_start" in cnv_calls.columns:
            cnv_calls = cnv_calls.sort_values(
                ["resolution", "CHROM", "window_start"],
                key=lambda s: s.map(chrom_sort_key) if s.name == "CHROM" else s,
            )
        else:
            cnv_calls = cnv_calls.sort_values(["resolution", "CHROM"], key=lambda s: s.map(chrom_sort_key) if s.name == "CHROM" else s)

    return by_chrom, by_window, cnv_calls


def estimate_cross_chain_error_rate(
    df: pd.DataFrame,
    min_mut_reads: int,
    single_chain_major_prop: float,
    fallback_error_rate: float,
) -> Tuple[float, int]:
    eligible = df[df["mut_total"] >= min_mut_reads].copy()
    if eligible.empty:
        return fallback_error_rate, 0

    single_like = eligible[eligible["mut_major_fraction"] >= single_chain_major_prop].copy()
    if single_like.empty:
        return fallback_error_rate, 0

    total_minor = single_like["mut_minor"].sum()
    total_mut = single_like["mut_total"].sum()
    if total_mut <= 0:
        return fallback_error_rate, 0

    return max(min(float(total_minor / total_mut), 0.20), 1e-6), int(len(single_like))


def detect_independent_recurrence(
    df: pd.DataFrame,
    min_total_mut_reads: int,
    min_minor_mut_reads: int,
    min_minor_mut_fraction: float,
    min_mut_reads_for_error_estimation: int,
    single_chain_major_prop: float,
    fallback_error_rate: float,
    alpha: float,
) -> Tuple[pd.DataFrame, float, int]:
    out = df.copy()

    out["mut_total"] = out["mut_chain_0"] + out["mut_chain_1"]
    out["mut_major"] = out[["mut_chain_0", "mut_chain_1"]].max(axis=1)
    out["mut_minor"] = out[["mut_chain_0", "mut_chain_1"]].min(axis=1)

    out["mut_major_chain"] = out.apply(
        lambda r: "chain_0" if r["mut_chain_0"] >= r["mut_chain_1"] else "chain_1", axis=1
    )
    out["mut_minor_chain"] = out.apply(
        lambda r: "chain_1" if r["mut_chain_0"] >= r["mut_chain_1"] else "chain_0", axis=1
    )

    out["mut_major_fraction"] = float("nan")
    has_mut = out["mut_total"] > 0
    out.loc[has_mut, "mut_major_fraction"] = out.loc[has_mut, "mut_major"] / out.loc[has_mut, "mut_total"]
    out["mut_minor_fraction"] = float("nan")
    out.loc[has_mut, "mut_minor_fraction"] = out.loc[has_mut, "mut_minor"] / out.loc[has_mut, "mut_total"]

    error_rate, n_sites_for_error = estimate_cross_chain_error_rate(
        out,
        min_mut_reads=min_mut_reads_for_error_estimation,
        single_chain_major_prop=single_chain_major_prop,
        fallback_error_rate=fallback_error_rate,
    )

    out["cross_chain_error_rate"] = error_rate
    out["expected_minor_from_error"] = out["mut_total"] * error_rate
    out["minor_tail_pvalue"] = out.apply(
        lambda r: _binom_sf(int(r["mut_minor"]), int(r["mut_total"]), error_rate) if r["mut_total"] > 0 else float("nan"),
        axis=1,
    )

    out["is_independent_recurrence_candidate"] = (
        (out["mut_total"] >= min_total_mut_reads)
        & (out["mut_minor"] >= min_minor_mut_reads)
        & (out["mut_minor_fraction"] >= min_minor_mut_fraction)
        & (out["minor_tail_pvalue"] <= alpha)
    )

    candidates = out[out["is_independent_recurrence_candidate"]].copy()
    candidates = candidates.sort_values(["minor_tail_pvalue", "mut_minor_fraction", "mut_total"], ascending=[True, False, False])

    cols = [
        "variant_id",
        "CHROM",
        "POS",
        "REF",
        "ALT",
        "n_mut_reads",
        "n_ref_reads",
        "mut_chain_0",
        "mut_chain_1",
        "mut_total",
        "mut_major",
        "mut_minor",
        "mut_major_chain",
        "mut_minor_chain",
        "mut_major_fraction",
        "mut_minor_fraction",
        "expected_minor_from_error",
        "cross_chain_error_rate",
        "minor_tail_pvalue",
        "is_independent_recurrence_candidate",
    ]

    return candidates[cols], error_rate, n_sites_for_error


def write_summary(
    out_path: Path,
    n_variants: int,
    n_informative: int,
    by_chrom: pd.DataFrame,
    by_window: pd.DataFrame,
    cnv_calls: pd.DataFrame,
    recurrence_candidates: pd.DataFrame,
    error_rate: float,
    n_sites_for_error: int,
) -> None:
    lines = [
        "Phase-output downstream analysis summary",
        "",
        f"Total variants analyzed: {n_variants}",
        f"Variants with informative chain coverage: {n_informative}",
        "",
        "Copy-number proxy (chain imbalance):",
        f"- Chromosome segments summarized: {len(by_chrom)}",
        f"- Window segments summarized: {len(by_window)}",
        f"- Non-balanced CNV proxy calls: {len(cnv_calls)}",
        "",
        "Independent recurrence (mutated reads in both chains):",
        f"- Estimated cross-chain error rate: {error_rate:.6f}",
        f"- Sites used for error-rate estimation: {n_sites_for_error}",
        f"- Candidate recurrent events: {len(recurrence_candidates)}",
        "",
        "Interpretation notes:",
        "- CNV calls here are proxies from chain imbalance at variant loci, not depth-based absolute copy number.",
        "- Because phase chains are arbitrary across disconnected components, CNV proxy is orientation-free (major vs minor chain support).",
        "- Recurrence candidates are filtered by support thresholds and a one-sided binomial test against estimated cross-chain noise.",
    ]
    out_path.write_text("\n".join(lines) + "\n")


def main() -> None:
    args = parse_args()

    input_path = Path(args.input)
    output_prefix = Path(args.output_prefix) if args.output_prefix else input_path.with_suffix("")

    df = pd.read_csv(input_path, sep="\t")
    missing = sorted(REQUIRED_COLUMNS - set(df.columns))
    if missing:
        raise ValueError(f"Missing required columns in input: {', '.join(missing)}")

    enriched = add_chain_coverage_fields(df)

    by_chrom, by_window, cnv_calls = infer_cnv_proxy(
        enriched,
        window_size=args.window_size,
        min_sites_per_segment=args.min_sites_per_segment,
        balance_tolerance=args.cnv_balance_tolerance,
        gain_threshold=args.cnv_gain_threshold,
        loss_threshold=args.cnv_loss_threshold,
    )

    recurrence_candidates, error_rate, n_sites_for_error = detect_independent_recurrence(
        enriched,
        min_total_mut_reads=args.min_total_mut_reads,
        min_minor_mut_reads=args.min_minor_mut_reads,
        min_minor_mut_fraction=args.min_minor_mut_fraction,
        min_mut_reads_for_error_estimation=args.min_mut_reads_for_error_estimation,
        single_chain_major_prop=args.single_chain_major_prop,
        fallback_error_rate=args.fallback_error_rate,
        alpha=args.alpha,
    )

    out_chrom = output_prefix.parent / f"{output_prefix.name}.copy_number_by_chromosome.tsv"
    out_window = output_prefix.parent / f"{output_prefix.name}.copy_number_by_window.tsv"
    out_calls = output_prefix.parent / f"{output_prefix.name}.copy_number_calls.tsv"
    out_recurrence = output_prefix.parent / f"{output_prefix.name}.independent_mutation_candidates.tsv"
    out_summary = output_prefix.parent / f"{output_prefix.name}.analysis_summary.txt"

    out_chrom.parent.mkdir(parents=True, exist_ok=True)

    by_chrom.to_csv(out_chrom, sep="\t", index=False)
    by_window.to_csv(out_window, sep="\t", index=False)
    cnv_calls.to_csv(out_calls, sep="\t", index=False)
    recurrence_candidates.to_csv(out_recurrence, sep="\t", index=False)

    write_summary(
        out_path=out_summary,
        n_variants=len(enriched),
        n_informative=int((enriched["total_chain_reads"] > 0).sum()),
        by_chrom=by_chrom,
        by_window=by_window,
        cnv_calls=cnv_calls,
        recurrence_candidates=recurrence_candidates,
        error_rate=error_rate,
        n_sites_for_error=n_sites_for_error,
    )

    print(f"Input variants: {len(enriched)}")
    print(f"Informative variants: {(enriched['total_chain_reads'] > 0).sum()}")
    print(f"Estimated cross-chain error rate: {error_rate:.6f} (from {n_sites_for_error} sites)")
    print(f"CNV proxy calls: {len(cnv_calls)}")
    print(f"Independent recurrence candidates: {len(recurrence_candidates)}")
    print(f"Wrote: {out_chrom}")
    print(f"Wrote: {out_window}")
    print(f"Wrote: {out_calls}")
    print(f"Wrote: {out_recurrence}")
    print(f"Wrote: {out_summary}")


if __name__ == "__main__":
    main()
